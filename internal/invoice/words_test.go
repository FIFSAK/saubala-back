package invoice

import "testing"

func TestTengeInWords(t *testing.T) {
	cases := []struct {
		tiyn int64
		want string
	}{
		// The grand total from the customer's sample invoice.
		{33872000, "триста тридцать восемь тысяч семьсот двадцать тенге ноль тиын"},
		{0, "ноль тенге ноль тиын"},
		{100, "один тенге ноль тиын"},
		{200, "два тенге ноль тиын"},
		{2100, "двадцать один тенге ноль тиын"},
		{111100, "одна тысяча сто одиннадцать тенге ноль тиын"},
		{200000, "две тысячи тенге ноль тиын"},
		{500000, "пять тысяч тенге ноль тиын"},
		{100050, "одна тысяча тенге пятьдесят тиын"},
		{123456789, "один миллион двести тридцать четыре тысячи пятьсот шестьдесят семь тенге восемьдесят девять тиын"},
	}
	for _, c := range cases {
		if got := TengeInWords(c.tiyn); got != c.want {
			t.Errorf("TengeInWords(%d) = %q, want %q", c.tiyn, got, c.want)
		}
	}
}
